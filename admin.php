    <script>
        let configData = {};
        let productsData = [];

        // DOM Elements
        const tbody = document.getElementById('products-tbody');
        const modal = document.getElementById('product-modal');
        const loader = document.getElementById('loader');
        const toast = document.getElementById('toast');

        window.onload = () => {
            // Load from global variables provided by products.js
            try {
                configData = JSON.parse(JSON.stringify(GADGON_CONFIG));
                productsData = JSON.parse(JSON.stringify(PRODUCTS));
            } catch (e) {
                console.error("Error loading initial data", e);
                showToast("Error loading initial data", true);
            }

            // Populate Config Fields
            document.getElementById('cfg-wa').value = configData.whatsappNumber || '';
            document.getElementById('cfg-name').value = configData.storeName || '';
            document.getElementById('cfg-greeting').value = configData.greeting || '';

            renderProductsTable();
        };

        function showToast(message, isError = false) {
            toast.textContent = message;
            toast.className = 'toast show';
            if (isError) toast.classList.add('error');
            setTimeout(() => {
                toast.classList.remove('show');
            }, 3000);
        }

        // --- Render Products ---
        function renderProductsTable() {
            tbody.innerHTML = '';
            productsData.forEach((p, index) => {
                const tr = document.createElement('tr');
                tr.innerHTML = `
                    <td><img src="${p.image}" class="product-img-thumb" onerror="this.src='assets/images/hero-banner.png'"></td>
                    <td style="font-weight: 500;">${p.name}</td>
                    <td><span style="background: rgba(255,255,255,0.1); padding: 4px 10px; border-radius: 20px; font-size: 12px;">${p.categoryLabel || p.category}</span></td>
                    <td style="color: var(--primary); font-weight: 600;">${p.price}</td>
                    <td>
                        <div class="product-actions">
                            <button class="btn btn-outline" style="padding: 6px 12px; font-size: 13px;" onclick="openProductModal(${index})">Edit</button>
                            <button class="btn btn-danger" style="padding: 6px 12px; font-size: 13px;" onclick="deleteProduct(${index})">Delete</button>
                        </div>
                    </td>
                `;
                tbody.appendChild(tr);
            });
        }

        // --- Modal Logic ---
        function openProductModal(index = null) {
            if (index !== null) {
                // Edit Mode
                const p = productsData[index];
                document.getElementById('modal-title').innerText = 'Edit Product';
                document.getElementById('prod-id').value = p.id;
                document.getElementById('prod-name').value = p.name;
                document.getElementById('prod-category').value = p.category || 'audio';
                document.getElementById('prod-categoryLabel').value = p.categoryLabel || '';
                document.getElementById('prod-price').value = p.price;
                document.getElementById('prod-oldPrice').value = p.oldPrice || '';
                document.getElementById('prod-badge').value = p.badge || '';
                document.getElementById('prod-image').value = p.image || '';
                document.getElementById('prod-desc').value = p.description || '';
                document.getElementById('prod-specs').value = (p.specs || []).join(', ');
            } else {
                // Add Mode
                document.getElementById('modal-title').innerText = 'Add New Product';
                document.getElementById('prod-id').value = 'new';
                document.getElementById('prod-name').value = '';
                document.getElementById('prod-category').value = 'audio';
                document.getElementById('prod-categoryLabel').value = '🎧 Audio';
                document.getElementById('prod-price').value = '';
                document.getElementById('prod-oldPrice').value = '';
                document.getElementById('prod-badge').value = '';
                document.getElementById('prod-image').value = '';
                document.getElementById('prod-desc').value = '';
                document.getElementById('prod-specs').value = '';
            }
            modal.classList.add('active');
        }

        function closeProductModal() {
            modal.classList.remove('active');
        }

        // Auto-update category label safely
        document.getElementById('prod-category').addEventListener('change', function() {
            const map = {
                'audio': '🎧 Audio', 'wearables': '⌚ Wearables', 'phones': '📱 Phones',
                'accessories': '⚡ Accessories', 'smart-home': '🏠 Smart Home'
            };
            if(map[this.value]) {
                document.getElementById('prod-categoryLabel').value = map[this.value];
            }
        });

        function saveProduct() {
            const idVal = document.getElementById('prod-id').value;
            const newProduct = {
                id: idVal === 'new' ? Date.now() : parseInt(idVal),
                name: document.getElementById('prod-name').value,
                category: document.getElementById('prod-category').value,
                categoryLabel: document.getElementById('prod-categoryLabel').value,
                price: document.getElementById('prod-price').value,
                oldPrice: document.getElementById('prod-oldPrice').value,
                badge: document.getElementById('prod-badge').value,
                image: document.getElementById('prod-image').value,
                description: document.getElementById('prod-desc').value,
                specs: document.getElementById('prod-specs').value.split(',').map(s => s.trim()).filter(s => s !== '')
            };

            if (idVal === 'new') {
                productsData.unshift(newProduct);
            } else {
                const idx = productsData.findIndex(p => p.id == idVal);
                if(idx > -1) productsData[idx] = newProduct;
            }

            renderProductsTable();
            closeProductModal();
            showToast("Product updated temporarily. Don't forget to 'Save Data'!", false);
        }

        function deleteProduct(index) {
            if(confirm('Are you sure you want to delete this product?')) {
                productsData.splice(index, 1);
                renderProductsTable();
            }
        }

        // --- Master Save JSON ---
        function saveAllData() {
            // Update Config from forms
            configData.whatsappNumber = document.getElementById('cfg-wa').value;
            configData.storeName = document.getElementById('cfg-name').value;
            configData.greeting = document.getElementById('cfg-greeting').value;

            const payload = {
                config: configData,
                products: productsData
            };

            loader.classList.add('active');

            fetch('admin.php?action=save_data', {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify(payload)
            })
            .then(res => res.json())
            .then(data => {
                loader.classList.remove('active');
                if (data.success) {
