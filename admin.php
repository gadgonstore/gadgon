        function uploadHeroImage() {
            if (!heroFile) return;
            const formData = new FormData();
            formData.append('hero_image', heroFile);

            document.getElementById('hero-status').innerText = 'Uploading...';
            document.getElementById('hero-save-btn').disabled = true;

            fetch('admin.php?action=save_hero', {
                method: 'POST',
                body: formData
            })
            .then(res => res.json())
            .then(data => {
                document.getElementById('hero-save-btn').disabled = false;
                if(data.success) {
                    document.getElementById('hero-save-btn').style.display = 'none';
                    document.getElementById('hero-status').innerText = 'Banner updated successfully!';
