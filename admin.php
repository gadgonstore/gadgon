if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    header('Content-Type: application/json');
    
    // Save Config & Products to products.js
    if (isset($_GET['action']) && $_GET['action'] === 'save_data') {
        $input = file_get_contents('php://input');
        $data = json_decode($input, true);
        if ($data && isset($data['config']) && isset($data['products'])) {
            $config_js = "const GADGON_CONFIG = " . json_encode($data['config'], JSON_PRETTY_PRINT | JSON_UNESCAPED_UNICODE) . ";\n";
            $products_js = "const PRODUCTS = " . json_encode($data['products'], JSON_PRETTY_PRINT | JSON_UNESCAPED_UNICODE) . ";\n";
            $js_output = "/**\n * GADGON – Products Data\n * Auto-generated by Admin Dashboard\n */\n\n" . $config_js . "\n" . $products_js;
            file_put_contents('assets/js/products.js', $js_output);
            echo json_encode(["success" => true]);
        } else {
            echo json_encode(["success" => false, "message" => "Invalid data"]);
        }
        exit;
    }

    // Upload Hero Image
    if (isset($_GET['action']) && $_GET['action'] === 'save_hero') {
        if (isset($_FILES['hero_image']) && $_FILES['hero_image']['error'] === UPLOAD_ERR_OK) {
            $tmp_name = $_FILES['hero_image']['tmp_name'];
            $ext = strtolower(pathinfo($_FILES['hero_image']['name'], PATHINFO_EXTENSION));
            if (in_array($ext, ['jpg', 'jpeg', 'png', 'webp', 'gif'])) {
                // Overwrite the existing hero banner directly
                $destination = 'assets/images/hero-banner.png';
                move_uploaded_file($tmp_name, $destination);
                echo json_encode(["success" => true, "path" => $destination . '?v=' . time()]);
            } else {
                echo json_encode(["success" => false, "message" => "Invalid file type. Only jpg, png, webp, gif allowed."]);
            }
        } else {
            echo json_encode(["success" => false, "message" => "Upload error"]);
        }
        exit;
    }

    // Upload Product Image
    if (isset($_GET['action']) && $_GET['action'] === 'upload_product_image') {
        if (isset($_FILES['product_image']) && $_FILES['product_image']['error'] === UPLOAD_ERR_OK) {
            $tmp_name = $_FILES['product_image']['tmp_name'];
            $ext = strtolower(pathinfo($_FILES['product_image']['name'], PATHINFO_EXTENSION));
            if (in_array($ext, ['jpg', 'jpeg', 'png', 'webp', 'gif'])) {
                $filename = 'prod_' . time() . '.' . $ext;
                $destination = 'assets/images/' . $filename;
                move_uploaded_file($tmp_name, $destination);
                echo json_encode(["success" => true, "path" => $destination]);
            } else {
                echo json_encode(["success" => false, "message" => "Invalid file type"]);
            }
        } else {
            echo json_encode(["success" => false, "message" => "Upload error"]);
        }
        exit;
    }
}
